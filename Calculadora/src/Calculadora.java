    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        painel.setVisible(true);
        float vl = (float) (valor.getValue());
        float resto = vl % 2;
        float eleCubico =  (float) Math.pow(vl,3);
        float raizQ = (float) Math.sqrt(vl);
        float raizC = (float) Math.cbrt(vl);
        float absoluto = (float) Math.abs(vl);
        
        lblResto.setText(Float.toString(resto));
        lblEleCubico.setText(String.format("%.2f",eleCubico));
        lblRaizQ.setText(String.format("%.2f", raizQ));
        lblRaizC.setText(String.format("%.2f",raizC));
        lblAbsoluto.setText(Float.toString(absoluto));
    }//GEN-LAST:event_btnCalcActionPerformed

